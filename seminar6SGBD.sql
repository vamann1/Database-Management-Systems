--FOLOSIND UN CURSOR SA SE AFISEZE NUMELE SI PRENUMELE CLIENTILOR CARE AU DAT CEL PUTIN DOUA COMENZI
--CLIENTII SE VOR AFISA IN ORDINEA DESC A LIMITEI DE CREDIT
--SE VOR AFISA DOAR PRIMII 5 ANGAJATI --DIN SELECT SAU CONDITIA DE IESIRE DIN LOOP, AICI SUNT AMBELE 
-- FETCH FIRST 5 ROWS WITH TIES : II IA SI PE ACEEASI CARE RESPECTA CRIETERIUL DE ORDONARE AL ULTIMULUI LUAT

DECLARE 
    CURSOR C IS SELECT NUME_CLIENT, PRENUME_CLIENT, LIMITA_CREDIT FROM CLIENTI WHERE ID_CLIENT IN (
SELECT ID_CLIENT FROM COMENZI GROUP BY ID_CLIENT HAVING COUNT(*)>=2)
    ORDER BY LIMITA_CREDIT DESC FETCH FIRST 5 ROWS ONLY;

    R C%ROWTYPE;
BEGIN 
    IF NOT C%ISOPEN THEN 
        OPEN C;
    END IF;
        LOOP
        FETCH C INTO R;
        EXIT WHEN C%NOTFOUND OR C%ROWCOUNT>5;
        DBMS_OUTPUT.PUT_LINE(C%ROWCOUNT || ' ' || R.NUME_CLIENT || ' ' || R.PRENUME_CLIENT || ' ' || R.LIMITA_CREDIT);
        END LOOP;
    CLOSE C;

END;
