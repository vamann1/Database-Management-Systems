SELECT CATEGORIE FROM PRODUSE WHERE ID_PRODUS IN (SELECT ID_PRODUS FROM RAND_COMENZI HAVING(SUM(PRET*CANTITATE)>50000) GROUP BY ID_PRODUS);



DECLARE 
CURSOR C IS SELECT CATEGORIE FROM PRODUSE P, RAND_COMENZI R WHERE P.ID_PRODUS=R.ID_PRODUS IN (SELECT ID_PRODUS FROM RAND_COMENZI HAVING(SUM(PRET*CANTITATE)>50000) GROUP BY ID_PRODUS);
BEGIN
    FOR R IN C LOOP
    DBMS_OUTPUT.PUT_LINE( R.CATEGORIE);
    END LOOP;
END;
