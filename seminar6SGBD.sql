--PARCURGEREA CLASICA CU OPEN-LOOP-FETCH-CLOSE 

DECLARE 
    CURSOR C IS SELECT NUME_CLIENT, PRENUME_CLIENT, LIMITA_CREDIT FROM CLIENTI WHERE ID_CLIENT IN (
SELECT ID_CLIENT FROM COMENZI GROUP BY ID_CLIENT HAVING COUNT(*)>=2)
    ORDER BY LIMITA_CREDIT DESC;
    V_NUME_CLIENT CLIENTI.NUME_CLIENT%TYPE;
    V_PRENUME_CLIENT CLIENTI.PRENUME_CLIENT%TYPE;
    V_LIMITA_CREDIT CLIENTI.LIMITA_CREDIT%TYPE;
BEGIN 
    IF NOT C%ISOPEN THEN 
        OPEN C;
    END IF;
        LOOP
        FETCH C INTO V_NUME_CLIENT, V_PRENUME_CLIENT, V_LIMITA_CREDIT;
        EXIT WHEN C%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE(C%ROWCOUNT || ' ' || V_NUME_CLIENT || ' ' || V_PRENUME_CLIENT || ' ' || V_LIMITA_CREDIT);
        END LOOP;
    CLOSE C;
END;
/

