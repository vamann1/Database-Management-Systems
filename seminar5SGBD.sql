FOLOSIND O COLECTIE SA SE AFISZE DENUMIREA PRODUSELOR, VALOAREA TOTALA A COMENZILOR 
PRECUM SI DATA ULTIMEI COMENZI PENTRU PRODUSUL RESPECTIV
SE VOR AFISA PRODUSELE ORDONATE DESCRESCATOR DUPA DATA ULTIMEI COMENZI 


DECLARE
  TYPE T_REC IS RECORD(
    DENUMIRE VARCHAR2(30),
    VAL_TOTALA NUMBER(20),
    DATA_ULTIMEI_COMENZI TIMESTAMP);
  TYPE T_PROD IS TABLE OF T_REC INDEX BY PLS_INTEGER; --INDEX BY TABLE/ASSOCIATIVE ARRAY
  V T_PROD;
  I PLS_INTEGER;
BEGIN
  SELECT P.DENUMIRE_PRODUS, SUM(R.PRET*R.CANTITATE), MAX(C.DATA) BULK COLLECT INTO V FROM RAND_COMENZI R, PRODUSE P, COMENZI C WHERE R.ID_PRODUS=P.ID_PRODUS AND R.ID_COMANDA=C.ID_COMANDA
   GROUP BY P.DENUMIRE_PRODUS
   ORDER BY MAX(C.DATA) DESC;
 
 I:=V.FIRST;
 WHILE I IS NOT NULL LOOP
   DBMS_OUTPUT.PUT_LINE(I||'->'||V(I).DENUMIRE||' '||V(I).VAL_TOTALA||' '||V(I).DATA_ULTIMEI_COMENZI);
   I:=V.NEXT(I);   
 END LOOP;
V.DELETE;
END;
/


