-- SA SE MAREASCA CU 5% SALARIUL ANG CARE AU INTERMEDIAT CEL PUTIN 2 COMENZI.
-- SA SE AFISEZE NUMARUL SALARIILOR MARITE

BEGIN 
    UPDATE ANGAJATI SET SALARIUL=SALARIUL*1.05 WHERE ID_ANGAJAT IN 
    ( SELECT ID_ANGAJAT FROM COMENZI GROUP BY ID_ANGAJAT HAVING COUNT(*)>=2);
    IF SQL%FOUND THEN 
        DBMS_OUTPUT.PUT_LINE('S-AU MODIFICAT SALARII');
    ELSE
        DBMS_OUTPUT.PUT_LINE('NU S-AU MODIFICAT SALARII');
    END IF;
    DBMS_OUTPUT.PUT_LINE(SQL%ROWCOUNT);

END;
/

-- SA SE MAREASCA CU 5% SALARIUL ANG CARE AU INTERMEDIAT CEL PUTIN 2 COMENZI.
-- SA SE AFISEZE NUMARUL SALARIILOR MARITE SI ID-URILE ANGAJATILOR CARE AU PRIMIT MARIRE

DECLARE
TYPE T_ANG IS TABLE OF NUMBER;
V T_ANG;

BEGIN 
    UPDATE ANGAJATI SET SALARIUL=SALARIUL*1.05 WHERE ID_ANGAJAT IN 
    ( SELECT ID_ANGAJAT FROM COMENZI GROUP BY ID_ANGAJAT HAVING COUNT(*)>=2) RETURNING ID_ANGAJAT BULK COLLECT INTO V;
    IF SQL%FOUND THEN 
        DBMS_OUTPUT.PUT_LINE('S-AU MODIFICAT SALARII');
    ELSE
        DBMS_OUTPUT.PUT_LINE('NU S-AU MODIFICAT SALARII');
    END IF;
    DBMS_OUTPUT.PUT_LINE(SQL%ROWCOUNT);
    
    FOR I IN 1..V.COUNT LOOP 
        DBMS_OUTPUT.PUT_LINE(V(I));
    END LOOP;
    ROLLBACK;
END;
/

