--FOLOSIND UN CURSOR SA SE MAREASCA CU 5% PRETUL DE LISTA PENTRU PRODUSELE 
--CARE APAR IN CEL PUTIN 5 COMENZI DISTINCTE


SELECT P.ID_PRODUS, DENUMIRE_PRODUS, PRET_LISTA FROM PRODUSE P JOIN RAND_COMENZI R
ON P.ID_PRODUS=R.ID_PRODUS
GROUP BY P.ID_PRODUS, DENUMIRE_PRODUS, PRET_LISTA
HAVING COUNT(*) >=5;

DECLARE
    CURSOR C IS SELECT P.ID_PRODUS, DENUMIRE_PRODUS, PRET_LISTA FROM PRODUSE P 
    WHERE P.ID_PRODUS IN (SELECT R.ID_PRODUS FROM RAND_COMENZI R 
            GROUP BY R.ID_PRODUS
            HAVING COUNT(*) >=5) FOR UPDATE WAIT 10; --SAU NOWAIT
BEGIN
    FOR R IN C LOOP 
    UPDATE PRODUSE SET PRET_LISTA = PRET_LISTA*1.05 WHERE CURRENT OF C;
    DBMS_OUTPUT.PUT_LINE(R.ID_PRODUS || ' ' || R.DENUMIRE_PRODUS || ' ' || R.PRET_LISTA); -- AFISEAZA DATELE DE LA DESCHIDEREA CURSORULUI, NU CELE ACTUALIZATE 
    END LOOP;
    EXCEPTION WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE('NU AM PUTUT BLOCA RANDURILE PENTRU ACTUALIZARE');
    
END;
