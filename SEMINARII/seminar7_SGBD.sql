--CURSORUL CU PARAMETRU
--SA SE AFISEZE NUMELE SI PRENUMELE CLIENTULUI CARE A DAT CELE MAI MULTE COMENZI
--SE VOR AFISA COMENZILE PENTRU RESPECTIVUL CLIENT  
    ID_COMANDA, DATA, VALOARE TOTALA A COMENZII

SELECT C.ID_CLIENT, PRENUME_CLIENT, NUME_CLIENT, COUNT(*) FROM CLIENTI C JOIN COMENZI O ON C.ID_CLIENT=O.ID_CLIENT
GROUP BY C.ID_CLIENT, PRENUME_CLIENT, NUME_CLIENT
ORDER BY COUNT(*) DESC FETCH FIRST 1 ROW WITH TIES;

SELECT C.ID_COMANDA, DATA, SUM(PRET*CANTITATE) VAL
FROM COMENZI C JOIN RAND_COMENZI R ON C.ID_COMANDA=R.ID_COMANDA
GROUP BY C.ID_COMANDA, DATA;

DECLARE
CURSOR C1 IS SELECT C.ID_CLIENT, PRENUME_CLIENT, NUME_CLIENT, COUNT(*) FROM CLIENTI C JOIN COMENZI O ON C.ID_CLIENT=O.ID_CLIENT
GROUP BY C.ID_CLIENT, PRENUME_CLIENT, NUME_CLIENT
ORDER BY COUNT(*) DESC FETCH FIRST 1 ROW WITH TIES;

CURSOR C2 (P_ID_CLIENT NUMBER) IS SELECT C.ID_COMANDA, DATA, SUM(PRET*CANTITATE) VAL
FROM COMENZI C JOIN RAND_COMENZI R ON C.ID_COMANDA=R.ID_COMANDA
WHERE C.ID_CLIENT=P_ID_CLIENT
GROUP BY C.ID_COMANDA, DATA;

BEGIN
    FOR R1 IN C1 LOOP
        DBMS_OUTPUT.PUT_LINE(R1.PRENUME_CLIENT || ': ' || R1.NUME_CLIENT);
        FOR R2 IN C2(R1.ID_CLIENT) LOOP
        DBMS_OUTPUT.PUT_LINE('   ' || R2.ID_COMANDA || '  ' || R2.DATA || ' ' || R2.VAL);
        END LOOP;
    END LOOP;

END;
