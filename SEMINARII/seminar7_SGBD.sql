--SA SE AFISEZE NUMELE SI PRENUMELE CLIENTULUI CARE ARE VALOAREA CEA MAI MARE A COMENZILOR
--SE VOR AFISA COMENZILE PENTRU RESPECTIVUL CLIENT  
    ID_COMANDA, DATA, VALOARE TOTALA A COMENZII
SELECT * FROM COMENZI;

SELECT CL.NUME_CLIENT || CL.PRENUME_CLIENT  FROM CLIENTI CL, COMENZI C, RAND_COMENZI R WHERE CL.ID_CLIENT=C.ID_CLIENT AND C.ID_COMANDA=R.ID_COMANDA
GROUP BY CL.NUME_CLIENT, CL.PRENUME_CLIENT
ORDER BY SUM(R.PRET*CANTITATE) DESC FETCH FIRST 1 ROW ONLY;


SELECT C.ID_COMANDA, C.DATA, SUM(R.PRET*CANTITATE) AS VALOARE_TOTALA FROM COMENZI C, RAND_COMENZI R WHERE C.ID_COMANDA=R.ID_COMANDA AND C.ID_CLIENT=(
SELECT ID_CLIENT  FROM CLIENTI WHERE NUME_CLIENT || PRENUME_CLIENT = (
SELECT CL.NUME_CLIENT || CL.PRENUME_CLIENT  FROM CLIENTI CL, COMENZI C, RAND_COMENZI R WHERE CL.ID_CLIENT=C.ID_CLIENT AND C.ID_COMANDA=R.ID_COMANDA
GROUP BY CL.NUME_CLIENT, CL.PRENUME_CLIENT
ORDER BY SUM(R.PRET*CANTITATE) DESC FETCH FIRST 1 ROW ONLY))
GROUP BY C.ID_COMANDA, C.DATA;

DECLARE 
CURSOR C IS SELECT CL.NUME_CLIENT, CL.PRENUME_CLIENT, SUM(R.PRET*CANTITATE) AS VALOARE_TOTALA FROM CLIENTI CL, COMENZI C, RAND_COMENZI R WHERE CL.ID_CLIENT=C.ID_CLIENT AND C.ID_COMANDA=R.ID_COMANDA
GROUP BY CL.NUME_CLIENT, CL.PRENUME_CLIENT
ORDER BY SUM(R.PRET*CANTITATE) DESC FETCH FIRST 1 ROW ONLY;

CURSOR D IS SELECT C.ID_COMANDA, C.DATA , SUM(R.PRET*CANTITATE) AS VALOARE_TOTALA FROM COMENZI C, RAND_COMENZI R WHERE C.ID_COMANDA=R.ID_COMANDA AND C.ID_CLIENT=(
SELECT ID_CLIENT  FROM CLIENTI WHERE NUME_CLIENT || PRENUME_CLIENT = (
SELECT CL.NUME_CLIENT || CL.PRENUME_CLIENT  FROM CLIENTI CL, COMENZI C, RAND_COMENZI R WHERE CL.ID_CLIENT=C.ID_CLIENT AND C.ID_COMANDA=R.ID_COMANDA
GROUP BY CL.NUME_CLIENT, CL.PRENUME_CLIENT
ORDER BY SUM(R.PRET*CANTITATE) DESC FETCH FIRST 1 ROW ONLY))
GROUP BY C.ID_COMANDA, C.DATA;

BEGIN
FOR R IN C LOOP
DBMS_OUTPUT.PUT_LINE(R.NUME_CLIENT || ' ' || R.PRENUME_CLIENT);
    FOR S IN D LOOP
    DBMS_OUTPUT.PUT_LINE('    ' || S.ID_COMANDA || ' ' || S.DATA || ' ' || S.VALOARE_TOTALA);
    END LOOP;
END LOOP;

END;
