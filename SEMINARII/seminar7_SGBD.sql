--FOLOSIND UN CURSOR SA SE AFISEZE DENUMIREA CATEGORIILOR DE PRODUSE SI VALOAREA TOTALA A COMENZILOR DIN RESPECTIVELE CATEGORII
--SE VOR AFISA DOAR CATEGORIILE CU VALOAR TOTALA A COMENZILOR > 50000
--PENTRU FIECARE CATEGORIE, SE VOR AFISA PRODUSELE DIN ACEA CATEGORIE SI VALOAREA TOTALA A COMENZILOR

SELECT CATEGORIE, produse FROM PRODUSE WHERE ID_PRODUS IN (SELECT ID_PRODUS FROM RAND_COMENZI HAVING(SUM(PRET*CANTITATE)>50000) GROUP BY ID_PRODUS);



DECLARE
 CURSOR C IS SELECT P.CATEGORIE, SUM(R.PRET*R.CANTITATE) VALOARE_TOTALA
 FROM PRODUSE P, RAND_COMENZI R WHERE P.ID_PRODUS=R.ID_PRODUS
 GROUP BY P.CATEGORIE, P.DENUMIRE_PRODUS, R.PRET*R.CANTITATE
 HAVING SUM(R.PRET*R.CANTITATE)>50000;
 CURSOR D IS SELECT P.DENUMIRE_PRODUS, CATEGORIE,  SUM(R.PRET*R.CANTITATE) AS VALOARE_TOTALA FROM PRODUSE P, RAND_COMENZI R WHERE P.ID_PRODUS=R.ID_PRODUS AND P.CATEGORIE=CATEGORIE
 GROUP BY CATEGORIE, P.DENUMIRE_PRODUS
 HAVING SUM(R.PRET*R.CANTITATE)>50000;
 
BEGIN
FOR R IN C LOOP
    DBMS_OUTPUT.PUT_LINE(R.CATEGORIE||': '||R.VALOARE_TOTALA);
    FOR S IN D LOOP 
    IF S.CATEGORIE=R.CATEGORIE THEN
    DBMS_OUTPUT.PUT_LINE('   ' || S.denumire_produs||': ' || S.VALOARE_TOTALA);
    END IF;
    END LOOP;
END LOOP;
END;
/

