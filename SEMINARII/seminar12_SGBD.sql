CREATE OR REPLACE TRIGGER MAXIM_REGIUNI AFTER INSERT ON REGIUNI
DECLARE
  N NUMBER;
BEGIN
  SELECT COUNT(*) INTO N FROM REGIUNI;
  IF N>=5 THEN
    RAISE_APPLICATION_ERROR(-20000,'PREA MULTE REGIUNI-'||N);
  END IF;
END;
