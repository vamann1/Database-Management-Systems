CREATE OR REPLACE TRIGGER VERIF_SAL BEFORE UPDATE OF SALARIUL ON ANGAJATI FOR EACH ROW
DECLARE 
  N NUMBER;
BEGIN
   DBMS_OUTPUT.PUT_LINE('SE MODIFICA SALARIUL PENTRU '||:OLD.ID_ANGAJAT||' DIN '||:OLD.SALARIUL||' IN '||:NEW.SALARIUL);   
   IF :NEW.SALARIUL<:OLD.SALARIUL THEN
     SELECT COUNT(*) INTO N FROM COMENZI C --JOIN ANGAJATI A ON C.ID_ANGAJAT=A.ID_ANGAJAT
     -- LA UN TRIGGER FOR EACH ROW, NU FOLOSITI INSTRUCTIUNI SQL PE TABELA PE CARE E DECLARAT TRIGGERUL-> MUTATING TRIGGER
     WHERE C.ID_ANGAJAT=:NEW.ID_ANGAJAT;
     IF N>=2 THEN
        RAISE_APPLICATION_ERROR(-20001,'NU SE POATE MICSORA SALARIUL PENTRU ACEST ANGAJAT-'||:NEW.ID_ANGAJAT);
     END IF;   
   END IF;
END;
