STERGEREA DE COMENZI DUPA ORA 16:20*/

CREATE OR REPLACE TRIGGER VERIF_ORA AFTER INSERT OR UPDATE OR DELETE ON COMENZI 
--DECLARE
--  N NUMBER;
BEGIN
  IF INSERTING AND TO_CHAR(SYSDATE,'HH24:MI')>='16:00' THEN
  RAISE_APPLICATION_ERROR(-20000,'NU SE POT ADAUGA COMENZI DUPA ORA 16');
  --DBMS_OUTPUT.PUT_LINE('S-A DECLANSAT TRIGGERUL INAINTEA UNEI OPERATII INSERT');
  ELSIF UPDATING AND TO_CHAR(SYSDATE,'HH24:MI')>='16:10' THEN
  RAISE_APPLICATION_ERROR(-20000,'NU SE POT MODIFICA COMENZI DUPA ORA 16:10');
  --ROLLBACK; COMMIT SI ROLLBACK NU SE FOLOSESC IN INTERIORUL UNUI TRIGGER
  --DBMS_OUTPUT.PUT_LINE('S-A DECLANSAT TRIGGERUL INAINTEA UNEI OPERATII UPDATE');
  ELSIF DELETING AND TO_CHAR(SYSDATE,'HH24:MI')>='16:20' THEN
  RAISE_APPLICATION_ERROR(-20000,'NU SE POT STERGE COMENZI DUPA ORA 16:20');
  --DBMS_OUTPUT.PUT_LINE('S-A DECLANSAT TRIGGERUL INAINTEA UNEI OPERATII DELETE');
  END IF;
END;
