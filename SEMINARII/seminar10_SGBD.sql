--FUNCTIILE DEFINITE POT FUNCTIONA CA SI FUNCTII SQL IN COMENZI SQL
SELECT ID_PRODUS, DENUMIRE_PRODUS, GET_VAL_COM_PRODUS(ID_PRODUS) VAL FROM PRODUSE
WHERE GET_VAL_COM_PRODUS(ID_PRODUS)>5000
ORDER BY GET_VAL_COM_PRODUS(ID_PRODUS) DESC;
    
DECLARE 
    CURSOR C IS SELECT ID_PRODUS, DENUMIRE_PRODUS, GET_VAL_COM_PRODUS(ID_PRODUS) VAL FROM PRODUSE
WHERE GET_VAL_COM_PRODUS(ID_PRODUS)>5000
ORDER BY GET_VAL_COM_PRODUS(ID_PRODUS) DESC;

BEGIN
    FOR R IN C LOOP
        DBMS_OUTPUT.PUT_LINE(R.ID_PRODUS || '->' || R.DENUMIRE_PRODUS || '->' || R.VAL);
    END LOOP;
END;
