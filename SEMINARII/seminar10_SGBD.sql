--A COMENZILOR PENTRU FIECARE PRODUS. DACA CATEG NU EXISTA, SA SE RIDICE O EXCEPTIE
--SA SE APELEZE PROEDURA SI SA SE TRATEZE RESPECTIVA EXCEPTIE IN BLOCUL APELATOR
