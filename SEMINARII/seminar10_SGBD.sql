CREATE OR REPLACE PROCEDURE AFISARE(P_CATEGORIE VARCHAR2) IS 
    CURSOR C IS SELECT P.DENUMIRE_PRODUS, SUM(R.CANTITATE * R.PRET) AS VALOARE FROM PRODUSE P, RAND_COMENZI R WHERE P.CATEGORIE=P_CATEGORIE AND P.ID_PRODUS=R.ID_PRODUS GROUP BY P.DENUMIRE_PRODUS;
    R C%ROWTYPE;
BEGIN
    OPEN C;
    LOOP
    FETCH C INTO R;
    EXIT WHEN C%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE('DENUMIRE:' ||  R.DENUMIRE_PRODUS || 'VALOARE: '||  R.VALOARE);
    END LOOP;
    DBMS_OUTPUT.put_line('Numar produse in categorie pentru care exista comenzi:' || C%ROWCOUNT);
    IF C%ROWCOUNT=0 THEN
        RAISE_APPLICATION_ERROR(-20000, 'NU SUNT PRODUSE IN ACEA CATEGORIE'); 
    END IF;

END;
/

DECLARE 
    NU_EXISTA_PROD EXCEPTION;
    PRAGMA EXCEPTION_INIT(NU_EXISTA_PROD, -20000);
    CATEG VARCHAR2(20) := 'hardwae1';
    
BEGIN
    AFISARE(P_CATEGORIE=>CATEG);
    EXCEPTION WHEN NU_EXISTA_PROD THEN 
        DBMS_OUTPUT.PUT_LINE('A APARUT O EXCEPTIE: ' || SQLERRM); 
END;
