CREATE OR REPLACE PROCEDURE MARESTE_PRET_PROD3(P_ID_PRODUS NUMBER default null, P_PROCENT NUMBER default 5, P_PRET_NOU OUT NUMBER) IS
    V_PRET NUMBER;
BEGIN
    UPDATE PRODUSE SET PRET_LISTA=PRET_LISTA*(100+P_PROCENT)/100 WHERE ID_PRODUS = NVL(P_ID_PRODUS, ID_PRODUS)
    RETURNING PRET_LISTA INTO P_PRET_NOU;
    EXCEPTION
        WHEN TOO_MANY_ROWS THEN 
            UPDATE PRODUSE SET PRET_LISTA=PRET_LISTA*(100+P_PROCENT)/100 WHERE ID_PRODUS = NVL(P_ID_PRODUS, ID_PRODUS);
            P_PRET_NOU:=SQL%ROWCOUNT; --VOI RETURNA NR DE RANDURI MODIFICATE, NU PRETUL NOU
END;
/

DECLARE 
    P NUMBER;
BEGIN
    MARESTE_PRET_PROD3(P_ID_PRODUS=>2378, P_PROCENT=>10, P_PRET_NOU=>P);
    IF P IS NOT NULL THEN
        DBMS_OUTPUT.PUT_LINE('PRETUL NOU ESTE: ' || P);
    END IF;
END;
/


