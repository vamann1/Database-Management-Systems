-- SA SE CONSTRUIASCA O FUNCTIE CARE PRIMESTE ID-UL UNUI PRODUS SI RETURNEAZA VALOAREA TOTALA A COMENZILOR PENTRU RESPECTIVUL PRODUS
-- SA SE APELEZE FUNCTIA DINTR-UN CURSOR

CREATE OR REPLACE FUNCTION GET_VAL_COM_PRODUS(P_ID_PRODUS NUMBER) RETURN NUMBER IS 
V_VAL NUMBER;
BEGIN
    SELECT SUM(PRET*CANTITATE) INTO V_VAL FROM RAND_COMENZI WHERE ID_PRODUS=P_ID_PRODUS;
    RETURN NVL(V_VAL, 0);
END;
/


