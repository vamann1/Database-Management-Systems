SA SE CONSTRUIASCA O PROCEDURA CARE PRIMESTE ID-UL UNUI PRODUS
SI UN PROCENT SI MARESTE CU RESPECTIVUL PROCENT PRETUL DE LISTA
A PRODUSULUI
SA SE AFISEZE PRETUL NOU

CREATE OR REPLACE PROCEDURE MARESTE_PRET_PROD(P_ID_PRODUS NUMBER, 
  P_PROC NUMBER DEFAULT 10)  
IS
V_PRET_NOU NUMBER;
BEGIN
  UPDATE PRODUSE SET PRET_LISTA=PRET_LISTA*(100+P_PROC)/100
    WHERE ID_PRODUS=P_ID_PRODUS;
  IF SQL%FOUND THEN  
  SELECT PRET_LISTA INTO V_PRET_NOU FROM PRODUSE
    WHERE ID_PRODUS=P_ID_PRODUS;
  DBMS_OUTPUT.PUT_LINE('S-A MARIT PRETUL CU '||P_PROC||'%');
  DBMS_OUTPUT.PUT_LINE('PRETUL NOU ESTE '||V_PRET_NOU);
  END IF;
END;
/

