CREATE OR REPLACE PROCEDURE MARESTE_PRET_PROD3(P_ID_PRODUS NUMBER, 
  P_PROC NUMBER DEFAULT 10, P_PRET_NOU OUT NUMBER)  
IS
BEGIN
  UPDATE PRODUSE SET PRET_LISTA=PRET_LISTA*(100+P_PROC)/100
    WHERE ID_PRODUS=P_ID_PRODUS RETURNING PRET_LISTA
    INTO P_PRET_NOU;
  IF SQL%FOUND THEN    
  DBMS_OUTPUT.PUT_LINE('S-A MARIT PRETUL CU '||P_PROC||'%');
  DBMS_OUTPUT.PUT_LINE('PRETUL NOU ESTE '||P_PRET_NOU);
  ELSE 
    RAISE_APPLICATION_ERROR(-20001,'NU EXISTA PRODUSUL '||P_ID_PRODUS);
  END IF;
END;
/

